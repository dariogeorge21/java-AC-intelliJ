package com.javabase;

public class Maths {
    public static void main(String[] args) {
        //max
        System.out.println(Math.max(5,9));
        //min
        System.out.println(Math.min(5,9));
        //random
        System.out.println(Math.random());
        //randomInt
        System.out.println((int)(Math.random()*100));
    }
}
